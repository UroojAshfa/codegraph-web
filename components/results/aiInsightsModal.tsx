"use client";

import { useState, useEffect } from "react";
import { Sparkles, X, Loader2, Lightbulb, AlertTriangle, Code2, Copy, Check } from "lucide-react";
import { AnalysisCache } from "@/lib/analysisCache";

interface AIInsightsModalProps {
  functionData: {
    name: string;
    complexity: number;
    file: string;
    line: number;
  };
  onClose: () => void;
}

export function AIInsightsModal({ functionData, onClose }: AIInsightsModalProps) {
  const [loading, setLoading] = useState(false);
  const [insights, setInsights] = useState<{
    explanation?: string;
    codeSmells?: string[];
    refactoringSuggestions?: string;
  } | null>(null);
  const [error, setError] = useState<string | null>(null);
  const [copied, setCopied] = useState(false);
  const [fromCache, setFromCache] = useState(false);

  // Check cache on mount
  useEffect(() => {
    const cached = AnalysisCache.get(
      functionData.name,
      functionData.file,
      functionData.line
    );

    if (cached) {
      setInsights(cached.insights);
      setFromCache(true);
    }
  }, [functionData]);

  const analyzeFunction = async () => {
    setLoading(true);
    setError(null);
    setFromCache(false);

    try {
      const response = await fetch("/api/ai-analyze", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({
          functionName: functionData.name,
          complexity: functionData.complexity,
          file: functionData.file,
          line: functionData.line,
        }),
      });

      if (!response.ok) {
        throw new Error("AI analysis failed");
      }

      const result = await response.json();
      setInsights(result.data);
      
      // Cache the results
      AnalysisCache.set(
        functionData.name,
        functionData.file,
        functionData.line,
        functionData.complexity,
        result.data
      );
    } catch (err) {
      setError(err instanceof Error ? err.message : "Failed to get AI insights");
    } finally {
      setLoading(false);
    }
  };

  const copyToClipboard = () => {
    if (!insights) return;
    
    const text = `AI Analysis: ${functionData.name}
Complexity: ${functionData.complexity}
File: ${functionData.file}:${functionData.line}

EXPLANATION:
${insights.explanation || 'N/A'}

CODE SMELLS:
${insights.codeSmells?.map(s => `• ${s}`).join('\n') || 'None detected'}

REFACTORING SUGGESTIONS:
${insights.refactoringSuggestions || 'N/A'}

---
Generated by CodeGraph AI`;

    navigator.clipboard.writeText(text);
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

  return (
    <div className="fixed inset-0 bg-black/50 backdrop-blur-sm z-50 flex items-center justify-center p-4">
      <div className="bg-white dark:bg-slate-800 rounded-xl shadow-2xl max-w-2xl w-full max-h-[80vh] overflow-hidden flex flex-col">
        
        {/* Header */}
        <div className="flex items-center justify-between p-6 border-b border-slate-200 dark:border-slate-700">
          <div className="flex items-center gap-3">
            <div className="p-2 bg-gradient-to-br from-purple-500 to-pink-500 rounded-lg">
              <Sparkles className="w-5 h-5 text-white" />
            </div>
            <div>
              <h2 className="text-xl font-bold text-slate-900 dark:text-white">
                AI Insights
              </h2>
              <p className="text-sm text-slate-600 dark:text-slate-400">
                <code className="bg-slate-100 dark:bg-slate-700 px-2 py-0.5 rounded">
                  {functionData.name}
                </code>
              </p>
            </div>
          </div>
          <button
            onClick={onClose}
            className="p-2 hover:bg-slate-100 dark:hover:bg-slate-700 rounded-lg transition-colors"
          >
            <X className="w-5 h-5 text-slate-600 dark:text-slate-400" />
          </button>
        </div>

        {/* Content */}
        <div className="flex-1 overflow-y-auto p-6">
          {!insights && !loading && !error && (
            <div className="text-center py-12">
              <div className="w-16 h-16 bg-gradient-to-br from-purple-500 to-pink-500 rounded-full flex items-center justify-center mx-auto mb-4">
                <Sparkles className="w-8 h-8 text-white" />
              </div>
              <h3 className="text-lg font-semibold text-slate-900 dark:text-white mb-2">
                Get AI-Powered Analysis
              </h3>
              
              {/* 🎯 COMPLEXITY BADGE - Added here */}
              <div className="flex justify-center mb-4">
                <span className={`inline-flex items-center gap-2 px-4 py-2 rounded-full text-sm font-semibold ${
                  functionData.complexity <= 10 
                    ? 'bg-green-100 text-green-700 dark:bg-green-900/30 dark:text-green-400' 
                    : functionData.complexity <= 20 
                    ? 'bg-amber-100 text-amber-700 dark:bg-amber-900/30 dark:text-amber-400'
                    : 'bg-red-100 text-red-700 dark:bg-red-900/30 dark:text-red-400'
                }`}>
                  Complexity: {functionData.complexity}
                  {functionData.complexity <= 10 && ' (Good)'}
                  {functionData.complexity > 10 && functionData.complexity <= 20 && ' (High)'}
                  {functionData.complexity > 20 && ' (Critical)'}
                </span>
              </div>

              <button
                onClick={analyzeFunction}
                className="px-6 py-3 bg-gradient-to-r from-purple-500 to-pink-500 text-white rounded-lg font-semibold hover:from-purple-600 hover:to-pink-600 transition-all"
              >
                Analyze with Gemini AI
              </button>

              {/* 🎯 COST TRANSPARENCY - Added here at bottom of initial state */}
              <p className="text-xs text-slate-500 dark:text-slate-600 mt-4">
                ⚡ Analysis powered by Google Gemini Pro
              </p>
            </div>
          )}

          {loading && (
            <div className="text-center py-12">
              <Loader2 className="w-12 h-12 animate-spin text-cyan-500 mx-auto mb-4" />
              <p className="text-slate-600 dark:text-slate-400">
                AI is analyzing your function...
              </p>
              <p className="text-xs text-slate-500 dark:text-slate-600 mt-2">
                This may take a few seconds
              </p>
            </div>
          )}

          {error && (
            <div className="p-4 bg-red-50 dark:bg-red-900/20 border border-red-200 dark:border-red-800 rounded-lg">
              <p className="text-red-600 dark:text-red-400">{error}</p>
            </div>
          )}

          {insights && (
            <div className="space-y-6">
              {/* 🎯 COPY BUTTON & CACHE INDICATOR - Added here at top of results */}
              <div className="flex items-center justify-between pb-4 border-b border-slate-200 dark:border-slate-700">
                <div className="flex items-center gap-2">
                  <span className={`inline-flex items-center gap-2 px-3 py-1.5 rounded-full text-xs font-semibold ${
                    functionData.complexity <= 10 
                      ? 'bg-green-100 text-green-700 dark:bg-green-900/30 dark:text-green-400' 
                      : functionData.complexity <= 20 
                      ? 'bg-amber-100 text-amber-700 dark:bg-amber-900/30 dark:text-amber-400'
                      : 'bg-red-100 text-red-700 dark:bg-red-900/30 dark:text-red-400'
                  }`}>
                    Complexity: {functionData.complexity}
                  </span>
                  <span className="text-xs text-slate-500 dark:text-slate-600">
                    ⚡ Gemini Pro
                  </span>
                  {fromCache && (
                    <span className="text-xs text-cyan-600 dark:text-cyan-400 bg-cyan-50 dark:bg-cyan-900/20 px-2 py-1 rounded">
                      📦 Cached
                    </span>
                  )}
                </div>
                <button
                  onClick={copyToClipboard}
                  className="inline-flex items-center gap-2 px-3 py-1.5 text-sm font-medium text-cyan-600 dark:text-cyan-400 hover:bg-cyan-50 dark:hover:bg-cyan-900/20 rounded-lg transition-colors"
                >
                  {copied ? (
                    <>
                      <Check className="w-4 h-4" />
                      <span>Copied!</span>
                    </>
                  ) : (
                    <>
                      <Copy className="w-4 h-4" />
                      <span>Copy Analysis</span>
                    </>
                  )}
                </button>
              </div>

              {/* Explanation */}
              {insights.explanation && (
                <div className="space-y-2">
                  <div className="flex items-center gap-2 mb-3">
                    <Code2 className="w-5 h-5 text-cyan-500" />
                    <h3 className="font-semibold text-slate-900 dark:text-white">
                      Function Explanation
                    </h3>
                  </div>
                  <div className="p-4 bg-slate-50 dark:bg-slate-900 rounded-lg">
                    <p className="text-slate-700 dark:text-slate-300 whitespace-pre-wrap">
                      {insights.explanation}
                    </p>
                  </div>
                </div>
              )}

              {/* Code Smells */}
              {insights.codeSmells && insights.codeSmells.length > 0 && (
                <div className="space-y-2">
                  <div className="flex items-center gap-2 mb-3">
                    <AlertTriangle className="w-5 h-5 text-amber-500" />
                    <h3 className="font-semibold text-slate-900 dark:text-white">
                      Code Smells Detected
                    </h3>
                  </div>
                  <ul className="space-y-2">
                    {insights.codeSmells.map((smell, i) => (
                      <li
                        key={i}
                        className="flex items-start gap-2 p-3 bg-amber-50 dark:bg-amber-900/20 border border-amber-200 dark:border-amber-800 rounded-lg"
                      >
                        <span className="text-amber-600 dark:text-amber-400">•</span>
                        <span className="text-slate-700 dark:text-slate-300">{smell}</span>
                      </li>
                    ))}
                  </ul>
                </div>
              )}

              {/* Refactoring Suggestions */}
              {insights.refactoringSuggestions && (
                <div className="space-y-2">
                  <div className="flex items-center gap-2 mb-3">
                    <Lightbulb className="w-5 h-5 text-green-500" />
                    <h3 className="font-semibold text-slate-900 dark:text-white">
                      Refactoring Suggestions
                    </h3>
                  </div>
                  <div className="p-4 bg-green-50 dark:bg-green-900/20 border border-green-200 dark:border-green-800 rounded-lg">
                    <p className="text-slate-700 dark:text-slate-300 whitespace-pre-wrap">
                      {insights.refactoringSuggestions}
                    </p>
                  </div>
                </div>
              )}
            </div>
          )}
        </div>
      </div>
    </div>
  );
}